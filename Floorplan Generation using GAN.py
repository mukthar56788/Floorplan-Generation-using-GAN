# --- Generator Model ---
class Generator(nn.Module):
    def __init__(self):
        super(Generator, self).__init__()
        self.text_embed = nn.Linear(text_embed_dim, 64)  # Project CLIP embedding
        self.main = nn.Sequential(
            nn.ConvTranspose2d(latent_dim + 64, 512, 4, 1, 0, bias=False),
            nn.BatchNorm2d(512),
            nn.ReLU(True),
            nn.ConvTranspose2d(512, 256, 4, 2, 1, bias=False),
            nn.BatchNorm2d(256),
            nn.ReLU(True),
            nn.ConvTranspose2d(256, 128, 4, 2, 1, bias=False),
            nn.BatchNorm2d(128),
            nn.ReLU(True),
            nn.ConvTranspose2d(128, 64, 4, 2, 1, bias=False),
            nn.BatchNorm2d(64),
            nn.ReLU(True),
            nn.ConvTranspose2d(64, 3, 4, 2, 1, bias=False),  # Output 3 channels (RGB)
            nn.Tanh()  # Output in [-1, 1]
        )

    def forward(self, noise, text_embeds):
        batch_size = noise.size(0)  # Get the batch size from noise
        text_out = self.text_embed(text_embeds)  # [batch_size, 64]
        text_out = text_out.view(batch_size, 64, 1, 1)  # [batch_size, 64, 1, 1]
        input_combined = torch.cat([noise, text_out], dim=1)  # Concatenate noise and text embedding
        return self.main(input_combined)

# --- Discriminator Model ---
class Discriminator(nn.Module):
    def __init__(self):
        super(Discriminator, self).__init__()
        self.text_embed = nn.Linear(text_embed_dim, 1 * 64 * 64)  # 512 to 4096
        self.main = nn.Sequential(
            nn.Conv2d(3 + 1, 64, 4, 2, 1, bias=False),  # 3 for RGB + 1 for text channel
            nn.LeakyReLU(0.2, inplace=True),
            nn.Conv2d(64, 128, 4, 2, 1, bias=False),
            nn.BatchNorm2d(128),
            nn.LeakyReLU(0.2, inplace=True),
            nn.Conv2d(128, 256, 4, 2, 1, bias=False),
            nn.BatchNorm2d(256),
            nn.LeakyReLU(0.2, inplace=True),
            nn.Conv2d(256, 512, 4, 2, 1, bias=False),
            nn.BatchNorm2d(512),
            nn.LeakyReLU(0.2, inplace=True),
            nn.Conv2d(512, 1, 4, 1, 0, bias=False),
            nn.Sigmoid()
        )

    def forward(self, img, text_embeds):
        batch_size = img.size(0)  # Get the batch size from img
        text_out = self.text_embed(text_embeds)  # [batch_size, 4096]
        text_out = text_out.view(batch_size, 1, 64, 64)  # [batch_size, 1, 64, 64]
        input_combined = torch.cat([img, text_out], dim=1)  # Concatenate image and text channel
        return self.main(input_combined).view(-1)

# --- Initialize Models and Optimizers ---
netG = Generator().to(device)
netD = Discriminator().to(device)
criterion = nn.BCELoss()
optD = optim.Adam(netD.parameters(), lr=lr, betas=(beta1, 0.999))
optG = optim.Adam(netG.parameters(), lr=lr, betas=(beta1, 0.999))

# --- Training Loop ---
losses_G, losses_D = [], []
for epoch in range(num_epochs):
    print(f'Epoch [{epoch+1}/{num_epochs}]')
    for i, (real_imgs, text_embeds) in enumerate(dataloader):
        real_imgs = real_imgs.to(device)
        text_embeds = text_embeds.to(device)
        batch_size = real_imgs.size(0)

        # --- Train Discriminator ---
        netD.zero_grad()
        real_labels = torch.full((batch_size,), 0.9, device=device)
        output_real = netD(real_imgs, text_embeds)
        errD_real = criterion(output_real, real_labels)

        noise = torch.randn(batch_size, latent_dim, 1, 1, device=device)
        fake_imgs = netG(noise, text_embeds)
        fake_labels = torch.full((batch_size,), 0.1, device=device)
        output_fake = netD(fake_imgs.detach(), text_embeds)
        errD_fake = criterion(output_fake, fake_labels)

        real_imgs = real_imgs + 0.02 * torch.randn_like(real_imgs)
        real_imgs.clamp_(-1, 1)

        errD = errD_real + errD_fake
        errD.backward()
        optD.step()

        # --- Train Generator ---
        netG.zero_grad()
        fake_labels = torch.full((batch_size,), 1.0, device=device)
        output = netD(fake_imgs, text_embeds)
        errG = criterion(output, fake_labels)
        errG.backward()
        optG.step()
        losses_G.append(errG.item())
        losses_D.append(errD.item())

    # Save model weights to Google Drive
    torch.save(netG.state_dict(), f'{MODEL_SAVE_PATH}/generator_epoch_{epoch+1}.pth')
    torch.save(netD.state_dict(), f'{MODEL_SAVE_PATH}/discriminator_epoch_{epoch+1}.pth')

    # Visualize generated images
    with torch.no_grad():
        fake_imgs = netG(noise, text_embeds).detach().cpu()
        plt.figure(figsize=(8, 8))
        plt.imshow(np.transpose(torchvision.utils.make_grid(fake_imgs[:16], padding=2, normalize=True), (1, 2, 0)))
        plt.axis('off')
        plt.title(f'Generated Floor Plans at Epoch {epoch+1}')
        plt.show()

