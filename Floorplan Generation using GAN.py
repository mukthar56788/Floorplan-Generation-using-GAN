# --- Custom Dataset for Floor Plans ---
class FloorPlanDataset(Dataset):
    def __init__(self, image_dir, annotation_dir, transform=None):
        self.image_dir = image_dir
        self.annotation_dir = annotation_dir
        self.transform = transform
        self.image_files = [f for f in os.listdir(image_dir) if f.endswith('.png')]
        self.image_files = [f for f in self.image_files if f.replace('.png', '.txt') in os.listdir(annotation_dir)]

        # Initialize CLIP
        self.tokenizer = CLIPTokenizer.from_pretrained('openai/clip-vit-base-patch32')
        self.text_model = CLIPTextModel.from_pretrained('openai/clip-vit-base-patch32').to(device)
        self.text_model.eval()

        # Precompute all text embeddings
        self.text_embeddings = []
        print("Precomputing text embeddings...")
        for img_name in self.image_files:
            txt_name = img_name.replace('.png', '.txt')
            txt_path = os.path.join(self.annotation_dir, txt_name)
            with open(txt_path, 'r') as f:
                text = f.read().strip()

            # Tokenize and encode text
            inputs = self.tokenizer(text, return_tensors="pt", padding=True, truncation=True, max_length=77)
            inputs = {k: v.to(device) for k, v in inputs.items()}
            with torch.no_grad():
                embedding = self.text_model(**inputs).last_hidden_state.mean(dim=1).squeeze(0)  # [512]
            self.text_embeddings.append(embedding.cpu())  # Store on CPU to save GPU memory

        self.text_embeddings = torch.stack(self.text_embeddings)  # [num_samples, 512]
        print(f"Text embeddings shape: {self.text_embeddings.shape}")
        assert len(self.image_files) == len(self.text_embeddings), "Mismatch between images and text embeddings!"

    def __len__(self):
        return len(self.image_files)

    def __getitem__(self, idx):
        # Load image
        img_name = self.image_files[idx]
        img_path = os.path.join(self.image_dir, img_name)
        image = Image.open(img_path)  # Keep as RGB

        # Apply image transforms
        if self.transform:
            image = self.transform(image)

        # Get precomputed text embedding
        text_embed = self.text_embeddings[idx]

        return image, text_embed

    def collate_fn(self, batch):
        images, text_embeds = zip(*batch)
        images = torch.stack(images)  # [batch_size, 3, 64, 64]
        text_embeds = torch.stack(text_embeds)  # [batch_size, 512]
        text_embeds = text_embeds.to(device)
        return images, text_embeds

